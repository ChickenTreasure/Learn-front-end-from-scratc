var num = 10

var result1 = 20
var result2 = 20


// 加法赋值运算符
result1 += num
result2 = result2 + num

console.log(result1 === result2) // true


// 减法赋值运算符
result1 -= num
result2 = result2 - num

console.log(result1 === result2) // true


// 指数赋值运算符
result1 **= num
result2 = result2 ** num

console.log(result1 === result2) // true

